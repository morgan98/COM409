     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        \Gloudemans\Shoppingcart\Facades\Cart::add($request->id, $request->name, 1, $request->price)
        ->associate('App\Product');

        return redirect()->route('checkout.index')->with('success_message', 'Item was added to cart!');
